temp = int(input("What's the temperature outside today?"))
print()
print(temp)

# Above 90: 
if temp > 90: 
  print("Whoa, it’s boiling!") 

# Above or equal to 80:
if temp >= 80: 
  print("It’s getting hot")

# Below freezing:
if temp < 32: 
  print("Brrr, you need a coat!")

# Below 60:
if temp < 60: 
  print("Don’t forget your sweater")

# Below 80:
if temp < 80: 
  print("A perfect day")

